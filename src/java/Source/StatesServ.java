    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
            JSONArray json = Source.Addresses.convertAddress();
          
          int offset;
           if (request.getParameter("offset") == null) {
               offset = 1;
           } else {
               offset = Integer.parseInt(request.getParameter("offset"));
           }
           
       
      Map<String, String> hashMap = new HashMap<String, String>();
              for (int i = offset-1; i < offset+5; i++){
                  try {
                  JSONObject state = json.getJSONObject(i);
                  hashMap.put(state.getString("short_name"), state.getString("name"));
                  } catch (JSONException e) {
                      break;
                  }
              }
              request.setAttribute("allStates", hashMap);
              String destination = "Task.jsp";
              RequestDispatcher requestDispatcher = request.getRequestDispatcher(destination);
              requestDispatcher.forward(request, response);
    }

